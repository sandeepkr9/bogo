package com.NIT;

public class dataType {

    public static void main(String[] args){
        int a = 234567;
        boolean b = true;
        char c = 'P';
        byte d = 55;
        long l = 345678908;
        float f = 8765495.0877F;
        double o = 956546787;
        System.out.println("value of int a =" +a);
        System.out.println("value of boolean b =" +b);
        System.out.println("value of char c =" +c);
        System.out.println("value of byte b =" +d);
        System.out.println("value of long l =" +l);
        System.out.println("value of float f =" +f);
        System.out.println("value of double o =" +o);
    }
}
